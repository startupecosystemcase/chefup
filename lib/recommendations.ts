export function getRecommendedSkills(position: string | undefined, currentSkills: string[] = []): string[] {
  if (!position) return []
  
  const skills = professionSkills[position as keyof typeof professionSkills] || []
  return skills.filter(skill => !currentSkills.includes(skill))
}

